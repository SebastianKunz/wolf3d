#include "libft.h"

void	ft_putnbrll(long long n)
{
	if (n == -9223372036854775807)
	{
		ft_putchar('-');
		ft_putchar('9');
		ft_putnbrll(223372036854775808);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbrll(n / 10);
		ft_putnbrll(n % 10);
	}
	else
		ft_putchar(n + '0');
}
