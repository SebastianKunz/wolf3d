#include "libft.h"

size_t	ft_lllen(long long n)
{
	size_t len;

	len = 1;
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	while ((n = (n / 10)) > 0)
		len++;
	return (len);
}
